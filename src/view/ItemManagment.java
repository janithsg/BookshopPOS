    private void refreshTbl(){
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            list= ItemController.getAllItems();
            for(ItemModel item:list){
                model.addRow(new Object[]{item.getItemId(),item.getDesc(),item.getBuyPrice(),item.getSellPrice(),item.getQty()});
            }
            idTxt.setText(ItemController.getNextId());
            nameTxt.setText("");
            buyTxt.setText("");
            sellTxt.setText("");
            quanTxt.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(ItemManagment.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ItemManagment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
