    private void addbBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbBtnActionPerformed
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
       
        Boolean p = null;
        
        if(idTxt.getText().length()==0 || nameTxt.getText().length()==0 || buyTxt.getText().length()==0 ||quanTxt.getText().length()==0){
            JOptionPane.showMessageDialog(this, "All the fields are required. Please Complete all the details", "Incomplete Data", JOptionPane.INFORMATION_MESSAGE);
        }else{
           
            try {
                p = ItemController.AddItem(new ItemModel(idTxt.getText(), nameTxt.getText(), Float.valueOf(sellTxt.getText()), Float.valueOf(buyTxt.getText()), Integer.parseInt(quanTxt.getText())));
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ItemManagment.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(p){
                JOptionPane.showMessageDialog(this, "New Item Added Successfully.", "Saved", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "New Item Adding Failed.", "Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
        refreshTbl();
        addbBtn.setEnabled(false);
        newBtn.setEnabled(true);
        
    }//GEN-LAST:event_addbBtnActionPerformed

    private void cancelBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBtn2ActionPerformed
         Boolean p = null;
        
        if(idTxt.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Item id is required. Please Complete all the details", "Incomplete Data", JOptionPane.INFORMATION_MESSAGE);
        }else{
           
            try {
                p = ItemController.DeleteItem(idTxt.getText());
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ItemManagment.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(p){
                JOptionPane.showMessageDialog(this, "Item delete Successfully.", "Saved", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Item delete Failed.", "Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
        refreshTbl();
    }//GEN-LAST:event_cancelBtn2ActionPerformed

