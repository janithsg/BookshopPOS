    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
            itemNo =itemIdCmb.getSelectedItem().toString();
            
            if(itemTbl.getRowCount()>0){
                if(PurchaseOrderController.addNewPurchaseOrder(new PurchaseOrderModel(pIdTxt.getText(), pDateTxt.getText(), tot,pList))){
                    JOptionPane.showMessageDialog(this, "Purchase Order Created Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    loadAllPurchases();
                }
                newPOrder();
            }else{
                JOptionPane.showMessageDialog(this, "No Items In the Order. Please Add Several Items And Try Again.", "Order Empty", JOptionPane.INFORMATION_MESSAGE);
            }
    }//GEN-LAST:event_saveBtnActionPerformed

