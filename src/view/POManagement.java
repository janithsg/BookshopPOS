    private void purTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_purTblMouseClicked
        
        try {
            int row=purTbl.rowAtPoint(evt.getPoint());
            String key=purTbl.getValueAt(row,0).toString();
            DefaultTableModel dtm=(DefaultTableModel)detailsTbl.getModel();
            ArrayList<PurchaseOrderDetailsModel> list=PurchaseOrderController.getAllDetails(key);
            dtm.setRowCount(0);
            for(PurchaseOrderDetailsModel pom:list){
                dtm.addRow(new Object[]{pom.getItemId(),pom.getQty(),pom.getItemTot()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(POManagement.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(POManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
           
    }//GEN-LAST:event_purTblMouseClicked

