     private void newPOrder(){
        DefaultTableModel dtm=(DefaultTableModel) itemTbl.getModel();
        dtm.setRowCount(0);
        pList=new ArrayList<>();
        pIdTxt.setText(PurchaseOrderController.creatNewId());
        setDate();
        loadItemsCombo();
        itemQtyTxt.setText("");
        tot=0;
        itemtot=0;
    }
      public void setDate(){
        SimpleDateFormat smdf = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss");
        String date = smdf.format(new Date());
        pDateTxt.setText(date);
    }
        private void loadItemsCombo(){
        try {
            items=ItemController.getAllItems();
            
            for(ItemModel item:items){
                itemIdCmb.addItem(item.getDesc());
            }
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        private void getItem(){
        String key=itemIdCmb.getSelectedItem().toString();
        
        for(ItemModel it:items){
            if(it.getDesc().equals(key)){
                item=new ItemModel(it.getItemId(),it.getDesc(),it.getBuyPrice(),it.getSellPrice(),it.getQty());
            }
        }
    }
private void clearall(){
    itemIdCmb.setSelectedIndex(0);
    uPriceTxt.setText("");
    itemQtyTxt.setText("");
    
}
