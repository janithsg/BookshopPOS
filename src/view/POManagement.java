    private void addItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemBtnActionPerformed
        DefaultTableModel dtm=(DefaultTableModel)itemTbl.getModel();
        
        dtm.addRow(new Object[]{item.getItemId(),item.getDesc(),itemQtyTxt.getText(),itemtot});
        tot=(Double.parseDouble(uPriceTxt.getText()) * Double.parseDouble(itemQtyTxt.getText()))+tot;
        ototTxt.setText(Double.toString(tot));
        pList.add(new PurchaseOrderDetailsModel(pIdTxt.getText(),item.getItemId(),Integer.parseInt(itemQtyTxt.getText()),itemtot));
        itemQtyTxt.setText("");
        ototTxt.setText("0.00");
    }//GEN-LAST:event_addItemBtnActionPerformed
    
    private void getSelectedItem(){
        String key=itemIdCmb.getSelectedItem().toString();
        
        for(ItemModel it:items){
            if(it.getDesc().equals(key)){
                item=new ItemModel(it.getItemId(),it.getDesc(),it.getBuyPrice(),it.getSellPrice(),it.getQty());
            }
        }
    }
    
