    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
            Boolean p = null;
        
        if(idTxt.getText().length()==0){
            JOptionPane.showMessageDialog(this, "All the fields are required. Please Complete all the details", "Incomplete Data", JOptionPane.INFORMATION_MESSAGE);
        }else{
           
            try {
                p = SupplierController.AddSupplier(new SupplierModel(idTxt.getText(), fnameTxt.getText(), lnameTxt.getText(),Integer.parseInt(phoneTxt.getText()),addrTxt.getText()) );
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ItemManagment.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(p){
                JOptionPane.showMessageDialog(this, "Data Added Successfully.", "Saved", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data Added Failed.", "Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
                     
    }//GEN-LAST:event_addBtnActionPerformed

