    private void refreshTbl(){
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            list= SupplierController.getAllItems();
            for(SupplierModel sup:list){
                model.addRow(new Object[]{sup.getSupId(),sup.getFname(),sup.getLname(),sup.getPhone(),sup.getAddress()});
            }
            idTxt.setText(ItemController.getNextId());
            idTxt.setText("");
            fnameTxt.setText("");
            lnameTxt.setText("");
            phoneTxt.setText("");
            addrTxt.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(ItemManagment.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ItemManagment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
